	function loadApp() {
		var displayMode = ""
		var heightPage = ""
		var widthPage = ""
		if(screen.width > 576){
			displayMode = "double"
			widthPage = "60%"
			heightPage = "85vh"
			$(".button").css({'display':'block'})
		}
		else{
			displayMode = "{{request()->double ? 'double' : 'single' }}"
			widthPage = "90%"
			heightPage = "65vh"
//			$("body").css({'background':'black'})
			$(".flipbook-viewport .container").css({'padding':'20px 0px'})
			$(".button").css({'display':'block'})
		}
		$('.flipbook').turn({
            duration : 2500,
			width:widthPage,
			height:heightPage,
			gradients: true,
			autoCenter: true,
			display: displayMode
		});
        $('.flipbook').bind('turning', function (event, page, view) {
            playPageChangeAudio(); // Play audio when turning the page
        });
		updateButtonStatus()
	}


	$('.button').on('click', function() {
        var type = $(this).data('type');
        if (type === 'prev') {
            $('.flipbook').turn('previous');
        } else if (type === 'next') {
            $('.flipbook').turn('next');
        }
		updateButtonStatus()
    });

    function updateButtonStatus() {
        var currentPage = $('.flipbook').turn('page');
        var totalPages = $('.flipbook').turn('pages');
        if (currentPage === 1) {
            $('[data-type="prev"]').prop('disabled', true);
        } else {
            $('[data-type="prev"]').prop('disabled', false);
        }

        if (currentPage === totalPages) {
            $('[data-type="next"]').prop('disabled', true);
        } else {
            $('[data-type="next"]').prop('disabled', false);
        }
	}

